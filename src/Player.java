public class Player {

    private int x;
    private int y;

    public Player(int px, int py) {
        this.x = px;
        this.y = py;
    }

    public int getX() {
        return x;
    }

    public void setX(int px) {
        this.x = px;
    }

    public int getY() {
        return y;
    }

    public void setY(int py) {
        this.y = py;
    }
}
